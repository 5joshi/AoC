def solve1(d):
    light = lmap(ints, d.splitlines())
    smallest, grid = 100, None
    for i in range(20000):
        points = {((px + (vx * i)), (py + (vy * i))) for px, py, vx, vy in light}
        if (d := max_minus_min(lmap(fst, points))) < smallest:
            smallest, grid = d, points_to_grid(points, flip=True)
    return "\n" + str(grid)

