def flip_vert(tile):
    tile.reverse()


def transpose(tile):
    tile = [[tile[j][i] for j in range(len(tile))]
            for i in range(len(tile[0]))]


def rotate_cw(tile):
    flip_vert(transpose(tile))


def solve(d):
    inp = d.split("\n\n")
    tiles = dict()
    for tile in inp:
        id = ints(tile)[0]
        tiles[id] = lmap(list, tile.splitlines()[1:])
        transpose(tiles[id])
    # pp.pprint(tiles)
    return


